def test_projection_is_deterministic_and_contains_no_downstream_claim_fields():
    first = build_local_provider_demo_projection()
    second = build_local_provider_demo_projection()

    assert first == second
    assert not (set(_walk_keys(first)) & FORBIDDEN_KEYS)


def test_rendered_javascript_is_deterministic_and_static():
    projection = build_local_provider_demo_projection()

    first = render_demo_data_javascript(projection)
    second = render_demo_data_javascript(projection)

    assert first == second
    assert first.startswith(
        "// Generated by scripts/build_prototype_local_provider_demo.py."
    )
    assert "window.KALIBRA_LOCAL_PROVIDER_DEMO =" in first
    assert str(DEFAULT_FIXTURE_PATH) not in first
    assert "accuracy" not in first
    assert "auc" not in first
    assert "benchmark" not in first
    assert "groundTruth" not in first
    assert '"calibratedConfidence": null' in first


def test_generated_static_data_file_is_byte_stable(tmp_path):
    output_path = tmp_path / "local-provider-demo-data.js"

    first_path = write_local_provider_demo_data(output_path)
    first = first_path.read_text(encoding="utf-8")
    second_path = write_local_provider_demo_data(output_path)
    second = second_path.read_text(encoding="utf-8")

    assert first_path == output_path.resolve()
    assert second_path == output_path.resolve()
    assert first == second
    payload = _extract_payload(first)
    assert payload["file"] == "blob_defect.pgm"
    assert payload["rawText"] == "75.00"
    assert payload["calibratedConfidence"] is None
    assert payload["qualifiedOutcome"] is None


def _sha256_file(path) -> str:
    digest = sha256()
    with path.open("rb") as file:
        for chunk in iter(lambda: file.read(1024 * 1024), b""):
            digest.update(chunk)
    return digest.hexdigest()


def _walk_keys(value):
    if isinstance(value, dict):
        for key, item in value.items():
            yield str(key)
            yield from _walk_keys(item)
    elif isinstance(value, list):
        for item in value:
            yield from _walk_keys(item)


def _extract_payload(source: str):
    prefix = "window.KALIBRA_LOCAL_PROVIDER_DEMO = "
    start = source.index(prefix) + len(prefix)
    payload = source[start:].strip()
    if payload.endswith(";"):
        payload = payload[:-1]
    return json.loads(payload)
