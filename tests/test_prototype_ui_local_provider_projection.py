def test_projection_uses_local_provider_and_engine_transformation():
    class RecordingProvider:
        def __init__(self) -> None:
            self.predictions: list[InspectionPrediction] = []
            self.inner = LocalArtifactInferenceProvider()

        def predict(self, inspection_input):
            prediction = self.inner.predict(inspection_input)
            self.predictions.append(prediction)
            return prediction

    class RecordingEngine:
        def __init__(self) -> None:
            self.calls: list[tuple[object, InspectionPrediction]] = []
            self.inner = InspectionEngine()

        def inspect(self, inspection_input):  # pragma: no cover
            raise AssertionError("prototype adapter must not call inspect")

        def transform_prediction(self, inspection_input, prediction):
            self.calls.append((inspection_input, prediction))
            return self.inner.transform_prediction(inspection_input, prediction)

    provider = RecordingProvider()
    engine = RecordingEngine()

    projection = build_local_provider_demo_projection(
        provider=provider,
        engine=engine,
    )

    assert len(provider.predictions) == 1
    assert isinstance(provider.predictions[0], InspectionPrediction)
    assert not isinstance(provider.predictions[0], RawInspectionResult)
    assert engine.calls == [(engine.calls[0][0], provider.predictions[0])]
    assert projection["examinationId"] == provider.predictions[0].prediction_id


